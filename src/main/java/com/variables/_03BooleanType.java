package com.variables;

public class _03BooleanType {

//-                                         PRIMITIVE TYPE boolean



//-                     The boolean variable type is the Java representation of a single bit
//-                     Boolean variables are used for binary decisions: true false


//!     TYPE          NAME        ASSIGNMENT OPERATOR           LITERAL
        boolean       aSwitch           =                        true;



//!     BITS  1                   POSSIBLE STATES               LITERAL RANGE

//`       1                         2                         true, false






//`                                       CHECKPOINT



//`                                 What will be the output of this program?


    static boolean lightSwitch = true;

    public static void main(String[] args) {

        if(lightSwitch){
            System.out.println("Light is on");
        }
        else{
            System.out.println("Light is off");
        }
    }









}