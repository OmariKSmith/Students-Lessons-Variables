package com.variables;

public class _02Variables {

//-                                Program states are represented by variables.

//-                                Variables are named memory allocations that store values / states.

//-                                In Java there are two familes of variables, primitive and object

//-                                Variables have to be 'declared' before they can store data.



//-                                              VARIABLE DECLARATION


//!    TYPE          NAME        ASSIGNMENT OPERATOR           LITERAL
       boolean       isOn                 =                    true;


                                                                                                         /*

`      TYPE:       Tells Java how much memory is needed to store the literal value

`      NAME:       A unique identifier that gives both Java and programmers
`                  the ability to distinguish between variables.
`                  (Always camelCase)
`                  (Must start with letter, underscore, or dollar sign)

`      ASSIGNMENT
`      OPERATOR:   Sets the variable reference on the left equal to the literal value on the right

`      LITERAL:    A single value you want the variable to hold or store
`
`                  Variable names  can only start with _, $, alphanumeric characters


*/




//`                                       CHECKPOINT


//                   Identify the TYPE in this variable declaration

       byte age = 127;





//`                  Identify the NAME in this variable declaration

       int phoneNumber = 5555555;




//                   Identify the LITERAL in this variable declaration

       char $gender = 'F';







//`                  Identify the TYPE in this variable declaration

       long wholePhoneNumber = 7775555555L;







//                   Identify the LITERAL in this variable declaration

       short _deviceID = 25768;







//`                  Identify the NAME in this variable declaration

       float height = 6.5F;







//                   Identify the TYPE in this variable declaration

       double $width = 6.5F;










}
